    private void initializeFormState() {
        dateSpinner1.setEditor(new JSpinner.DateEditor(dateSpinner1, "yyyy-MM-dd"));
        dateSpinner2.setEditor(new JSpinner.DateEditor(dateSpinner2, "yyyy-MM-dd"));
        statusComboBox.setModel(new DefaultComboBoxModel<>(new String[]{"Present", "Absent"}));

        AttendanceTable.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                fillFormFromSelectedRow();
            }
        });

        clearForm();
    }

    private void loadAttendanceRecords(Date filterDate) {
        try {
            DefaultTableModel model = (DefaultTableModel) AttendanceTable.getModel();
            model.setRowCount(0);

            for (Object[] row : attendanceService.getAttendanceRecords(filterDate)) {
                model.addRow(row);
            }
        } catch (SQLException ex) {
            showDatabaseError("Unable to load attendance records.", ex);
        }
    }

    private void fillFormFromSelectedRow() {
        int selectedRow = AttendanceTable.getSelectedRow();
        if (selectedRow < 0) {
            return;
        }

        DefaultTableModel model = (DefaultTableModel) AttendanceTable.getModel();
        studentIDTXT.setText(String.valueOf(model.getValueAt(selectedRow, 0)));
        nameTXT.setText(String.valueOf(model.getValueAt(selectedRow, 1)));
        statusComboBox.setSelectedItem(String.valueOf(model.getValueAt(selectedRow, 3)));

        Object dateValue = model.getValueAt(selectedRow, 2);
        if (dateValue instanceof Date) {
            Date selectedDate = (Date) dateValue;
            dateSpinner1.setValue(new java.util.Date(selectedDate.getTime()));
        } else if (dateValue != null) {
            try {
                Date parsedDate = Date.valueOf(dateValue.toString());
                dateSpinner1.setValue(new java.util.Date(parsedDate.getTime()));
            } catch (IllegalArgumentException ex) {
                // Ignore invalid date text from table cell.
            }
        }
    }

    private Date getDateFromSpinner(JSpinner spinner) {
        java.util.Date utilDate = (java.util.Date) spinner.getValue();
        return new Date(utilDate.getTime());
    }

    private void clearForm() {
        studentIDTXT.setText("");
        nameTXT.setText("");
        statusComboBox.setSelectedIndex(0);
        dateSpinner1.setValue(new java.util.Date());
        AttendanceTable.clearSelection();
    }

    private void showDatabaseError(String message, SQLException ex) {
        JOptionPane.showMessageDialog(
                this,
                message + "\n" + ex.getMessage(),
                "Database Error",
                JOptionPane.ERROR_MESSAGE
        );
    }

