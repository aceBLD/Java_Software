    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String studentId = studentIDTXT.getText().trim();
        String name = nameTXT.getText().trim();
        String status = (String) statusComboBox.getSelectedItem();
        Date attendanceDate = getDateFromSpinner(dateSpinner1);

        if (studentId.isEmpty() || name.isEmpty() || status == null) {
            JOptionPane.showMessageDialog(this, "Student ID, Name, Date, and Status are required.");
            return;
        }

        try {
            if (attendanceService.attendanceExists(studentId, attendanceDate)) {
                JOptionPane.showMessageDialog(this, "Duplicate attendance: this student already has a record for that date.");
                return;
            }

            attendanceService.addAttendance(studentId, name, attendanceDate, status);
            JOptionPane.showMessageDialog(this, "Attendance record added.");
            loadAttendanceRecords(null);
            clearForm();
        } catch (SQLException ex) {
            if (Objects.equals(ex.getSQLState(), "23505")) {
                JOptionPane.showMessageDialog(this, "Duplicate attendance: this student already has a record for that date.");
            } else {
                showDatabaseError("Unable to add attendance.", ex);
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        String studentId = studentIDTXT.getText().trim();
        String name = nameTXT.getText().trim();
        String status = (String) statusComboBox.getSelectedItem();
        Date attendanceDate = getDateFromSpinner(dateSpinner1);

        if (studentId.isEmpty() || name.isEmpty() || status == null) {
            JOptionPane.showMessageDialog(this, "Select a row first or fill all fields.");
            return;
        }

        try {
            int updatedRows = attendanceService.updateAttendance(studentId, name, attendanceDate, status);
            if (updatedRows > 0) {
                JOptionPane.showMessageDialog(this, "Attendance record updated.");
                loadAttendanceRecords(null);
            } else {
                JOptionPane.showMessageDialog(this, "No matching record found to update.");
            }
        } catch (SQLException ex) {
            showDatabaseError("Unable to update attendance.", ex);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        String studentId = studentIDTXT.getText().trim();
        Date attendanceDate = getDateFromSpinner(dateSpinner1);

        if (studentId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student ID is required to delete.");
            return;
        }

        try {
            int deletedRows = attendanceService.deleteAttendance(studentId, attendanceDate);
            if (deletedRows > 0) {
                JOptionPane.showMessageDialog(this, "Attendance record deleted.");
                loadAttendanceRecords(null);
                clearForm();
            } else {
                JOptionPane.showMessageDialog(this, "No matching record found to delete.");
            }
        } catch (SQLException ex) {
            showDatabaseError("Unable to delete attendance.", ex);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

